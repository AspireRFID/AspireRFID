    private void btOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOKActionPerformed
        if ("".equals(txLRSpec.getText().trim()) || "".equals(txLRSpecFile.getText())) {
             JOptionPane.showMessageDialog(null, "Please inform the name and path of the LRSpec",MESSAGE_TITLE,JOptionPane.WARNING_MESSAGE);
        } else {
            //TODO show message if error
            model.addLogicalReader(txLRSpec.getText(), txLRSpecFile.getText());
            lrspecsList.setModel(new LRSpecsModel());
        }
}//GEN-LAST:event_btOKActionPerformed

    private void btBrowseLRSpecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBrowseLRSpecActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new FileFilter() {
            public boolean accept(File f) {
                return f.isDirectory() || f.getName().toLowerCase().endsWith(".xml");
            }

            public String getDescription() {
                return ".xml files";
            }
        });
        int returnVal = fileChooser.showOpenDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            txLRSpecFile.setText(file.getAbsolutePath());
            txLRSpecFile.setToolTipText(file.getAbsolutePath());
        }
}//GEN-LAST:event_btBrowseLRSpecActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        String lrspec = (String)lrspecsList.getSelectedValue();

        if (lrspec != null) {
            model.deleteLogicalReader(lrspec);
            //TODO temp solution while not having push events
            SwingUtilities.invokeLater(new Runnable() {
                public void run() {
                    //refresh data
                    model.getLrSpecs(true);
                    lrspecsList.setModel(new LRSpecsModel());
                }
            });
            
        } else {
           JOptionPane.showMessageDialog(null, "Please select the LRSpec to be deleted",MESSAGE_TITLE,JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btDeleteActionPerformed


