   class MapToTableModelAdapter extends AbstractTableModel {
        private ArrayList<Map.Entry> entries;
        private static final int COLUMNS = 2;

        public MapToTableModelAdapter(Map map) {
            this.entries = new ArrayList<Map.Entry>(map.entrySet());
        }

        public String getColumnName(int column) {
            if (column == 0) {
                return "Key";
            } else {
                return "Value";
            }
        }


        public int getRowCount() {
            return entries.size();
        }

        public int getColumnCount() {
            return COLUMNS;
        }

        public Object getValueAt(int rowIndex, int columnIndex) {
            if (columnIndex == 0) {
                return entries.get(rowIndex).getKey();
            } else {
                return entries.get(rowIndex).getValue();
            }
        }

   }
