package org.ow2.aspirerfid.nfc.picking.tests;

import javax.microedition.lcdui.Display;
import javax.microedition.lcdui.Form;
import javax.microedition.midlet.MIDlet;
import javax.microedition.midlet.MIDletStateChangeException;

/**
 * This class test that a simple midlet has been created and well deployed. This
 * class does not use the GenericMidlet that is necessary to use the
 * architecture. It's just a test for deployment.
 * 
 * @author <a href="mailto:angoca@yahoo.com">Andres Gomez</a>
 * @version 1.0.0 27/08/2008
 */
public class TestMidlet extends MIDlet {
	/**
	 * Form where the messages will be printed.
	 */
	private Form m_form = null;

	/*
	 * (non-Javadoc)
	 * 
	 * @see javax.microedition.midlet.MIDlet#startApp()
	 */
	protected void startApp() throws MIDletStateChangeException {

		this.m_form = new Form("Test Midlet");
		Display.getDisplay(this).setCurrent(this.m_form);
		this.m_form.append("Hello world\n");
		this.m_form.append("The midlet has been well deployed.\n");
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see javax.microedition.midlet.MIDlet#destroyApp(boolean)
	 */
	protected void destroyApp(boolean arg0) throws MIDletStateChangeException {
		// Do nothing
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see javax.microedition.midlet.MIDlet#pauseApp()
	 */
	protected void pauseApp() {
		// Do nothing
	}
}
