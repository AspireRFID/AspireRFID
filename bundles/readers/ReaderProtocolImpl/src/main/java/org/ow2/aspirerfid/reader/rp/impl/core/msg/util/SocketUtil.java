package org.ow2.aspirerfid.reader.rp.impl.core.msg.util;

import java.io.IOException;
import java.net.ServerSocket;

/**
 * Utility class for socket related stuff.
 * 
 * @author Andreas Fï¿½rer, ETH Zurich Switzerland, Winter 2005/06
 *
 */

public class SocketUtil {
	/**
	 * Returns a free port number on localhost, or -1 if unable to find a free port.
	 * 
	 * @return a free port number > 1024 on localhost, or -1 if unable to find a free port
	 */
	public static int findFreePort() {
		ServerSocket socket= null;
		try {
			socket= new ServerSocket(0);
			return socket.getLocalPort();
		} catch (IOException e) { 
		} finally {
			if (socket != null) {
				try {
					socket.close();
				} catch (IOException e) {
				}
			}
		}
		return -1;		
	}	
}
